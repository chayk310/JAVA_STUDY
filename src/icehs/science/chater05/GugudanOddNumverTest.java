package icehs.science.chater05;

public class GugudanOddNumverTest {

	public static void main(String[] args) {
		int dan = 2;
		while ( dan <= 5) {
			int i = 1;
			while ( i <= 9 ) {
				System.out.println( dan + " * " + i + " = " + dan * i);
				i++;
			}
			dan ++;
		}
//고쳐야함
	}

}
